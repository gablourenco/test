namespace OHCE
{
    public static class ILangue
    {
        public français (LangueFrançaise langueF) {
            /// <inheritdoc />
            direBonjour ==> LangueFrançaise.DireBonjour;

            /// <inheritdoc />
            bienDit ==> LangueFrançaise.BienDit;
            
            /// <inheritdoc />
            auRevoir ==> LangueFrançaise.AuRevoir;
        }

        public anglais (LangueAnglaise langueA) {
            /// <inheritdoc />
            direBonjour ==> LangueAnglaise.DireBonjour;

            /// <inheritdoc />
            bienDit ==> LangueAnglaise.BienDit;
            
            /// <inheritdoc />
            auRevoir ==> LangueAnglaise.AuRevoir;
        }
    }
}
